    private void ucitaj() {
        DefaultListModel<Aviokompanija> m = new DefaultListModel<>();
        m.addAll(obrada.read(txtUvjet.getText(), true));
        lstAviokompanije.setModel(m);
        lstAviokompanije.repaint();
    }

