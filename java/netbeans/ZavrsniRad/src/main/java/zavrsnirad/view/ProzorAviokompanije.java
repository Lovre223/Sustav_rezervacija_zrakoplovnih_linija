    private void lstAviokompanijeValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstAviokompanijeValueChanged
        if(lstAviokompanije.getValueIsAdjusting()){
        
                return;
        
        }
        
        if(lstAviokompanije.getSelectedValue()== null){
        
                return;
        }

        obrada.setEntitet(lstAviokompanije.getSelectedValue());

        napuniView();


    }//GEN-LAST:event_lstAviokompanijeValueChanged

    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        obrada.setEntitet(new Aviokompanija());

        dodajNovi();
        try {
            obrada.create();
            ucitaj();
        } catch (AppException ex) {

            JOptionPane.showMessageDialog(getRootPane(), ex.getPoruka());
        }


    }//GEN-LAST:event_btnDodajActionPerformed

    private void btnBrisanjeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBrisanjeActionPerformed
        if(lstAviokompanije.getSelectedValue() == null){
                    JOptionPane.showMessageDialog(getRootPane(), "Aviokompanija za brisanje mora biti odabrana!!!");
                  return;  
                    
        }
        
        if(JOptionPane.showConfirmDialog(getRootPane(), "Sigurno Å¾elite obrisati " + obrada.getEntitet().getNaziv() + "?", "Brisanje", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE)== JOptionPane.NO_OPTION ){
            
                   return;
    }
        
        try {
            
            obrada.delete();
            ucitaj();
            
        } catch (AppException ex) {
        
            JOptionPane.showMessageDialog(getRootPane(), ex.getPoruka());
        }
        
        
        
        
        
    }//GEN-LAST:event_btnBrisanjeActionPerformed

